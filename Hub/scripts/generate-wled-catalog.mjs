/**
 * Fetches WLED FX.h + FX.cpp from wled-dev/wled (main) and emits TypeScript seed data
 * for Hub/electron/wled-effects-seed.generated.ts
 *
 * Effect metadata format: https://kno.wled.ge/interfaces/json-api/#effect-metadata
 *
 * Run from repo: node Hub/scripts/generate-wled-catalog.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import https from 'node:https';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const HUB_ROOT = path.join(__dirname, '..');

const FX_H_URL = 'https://raw.githubusercontent.com/wled-dev/wled/main/wled00/FX.h';
const FX_CPP_URL = 'https://raw.githubusercontent.com/wled-dev/wled/main/wled00/FX.cpp';

function fetchText(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
        fetchText(res.headers.location).then(resolve).catch(reject);
        return;
      }
      if (res.statusCode !== 200) {
        reject(new Error(`${url} -> ${res.statusCode}`));
        return;
      }
      const chunks = [];
      res.on('data', (c) => chunks.push(c));
      res.on('end', () => resolve(Buffer.concat(chunks).toString('utf8')));
    }).on('error', reject);
  });
}

const SLIDER_DEFAULTS = ['Effect speed', 'Effect intensity', 'Custom 1', 'Custom 2', 'Custom 3'];
const CHECK_DEFAULTS = ['Option 1', 'Option 2', 'Option 3'];

function parseFxCpp(cpp) {
  const re = /static const char (_data_FX_MODE_\w+)\s*\[\]\s*PROGMEM\s*=\s*"((?:[^"\\]|\\.)*)"\s*;/g;
  const map = new Map();
  let m;
  while ((m = re.exec(cpp)) !== null) {
    const key = m[1].replace('_data_FX_MODE_', 'FX_MODE_');
    map.set(key, m[2].replace(/\\"/g, '"'));
  }
  return map;
}

function parseFxH(h) {
  const idByMode = new Map();
  for (const line of h.split(/\r?\n/)) {
    const m = line.match(/^#define (FX_MODE_\w+)\s+(\d+)/);
    if (m) idByMode.set(m[1], Number(m[2]));
  }
  return idByMode;
}

function parseMetadataString(raw) {
  const at = raw.indexOf('@');
  const name = at >= 0 ? raw.slice(0, at) : raw;
  const meta = at >= 0 ? raw.slice(at + 1) : '';
  const parts = meta.split(';');
  const param = parts[0] ?? '';
  const flags = (parts[3] ?? '').trim() || '1';
  const paramTokens = param.split(',');

  const labels = [];
  for (let i = 0; i < paramTokens.length; i++) {
    const t = paramTokens[i].trim();
    if (!t) continue;
    if (t === '!') {
      if (i < 5) labels.push(SLIDER_DEFAULTS[i]);
      else labels.push(CHECK_DEFAULTS[i - 5]);
    } else {
      labels.push(t);
    }
  }

  let dimension = '3d';
  if (flags.includes('2')) dimension = '2d';
  else if (flags.includes('3')) dimension = '3d';

  let audioReactive = null;
  if (flags.includes('v')) audioReactive = 'volume';
  else if (flags.includes('f')) audioReactive = 'fft';

  return { name, metadata: raw, parameters: labels, dimension, flags, audioReactive };
}

async function main() {
  const [h, cpp] = await Promise.all([fetchText(FX_H_URL), fetchText(FX_CPP_URL)]);
  if (!h.length || !cpp.length) throw new Error('Empty fetch');
  const idByMode = parseFxH(h);
  const dataByMode = parseFxCpp(cpp);
  console.error('FX modes:', idByMode.size, 'data strings:', dataByMode.size);
  const rows = [];
  for (const [mode, id] of idByMode) {
    const raw = dataByMode.get(mode);
    if (raw === undefined) continue;
    const parsed = parseMetadataString(raw);
    rows.push({
      id,
      name: parsed.name,
      metadata: parsed.metadata,
      parameters: parsed.parameters,
      dimension: parsed.dimension,
      flags: parsed.flags,
      audioReactive: parsed.audioReactive
    });
  }
  rows.sort((a, b) => a.id - b.id);

  const outPath = path.join(HUB_ROOT, 'electron', 'wled-effects-seed.generated.ts');
  const header = `/* eslint-disable */\n/**
 * AUTO-GENERATED by scripts/generate-wled-catalog.mjs — do not edit by hand.
 * Source: wled-dev/wled main (FX.h + FX.cpp)
 */\n\n`;
  const body =
    'export type WledEffectSeedRow = {\n' +
    '    id: number;\n' +
    '    name: string;\n' +
    '    metadata: string;\n' +
    '    parameters: string[];\n' +
    "    dimension: '2d' | '3d';\n" +
    '    flags: string;\n' +
    "    audioReactive: 'volume' | 'fft' | null;\n" +
    '};\n\n' +
    `export const WLED_EFFECTS_SEED: WledEffectSeedRow[] = ${JSON.stringify(rows, null, 4)};\n`;

  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, header + body, 'utf8');
  console.log('Wrote', outPath, `(${rows.length} effects)`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
